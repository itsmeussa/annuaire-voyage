// Get coordinates for a city, with fallback to country center
export function getCityCoordinates(city: string | null, countryCode: string | null): [number, number] | null {
  if (!city) return getCountryCenter(countryCode);
  
  // Direct match
  if (cityCoordinates[city]) {
    return cityCoordinates[city];
  }
  
  // Try lowercase match
  const lowerCity = city.toLowerCase();
  for (const [key, coords] of Object.entries(cityCoordinates)) {
    if (key.toLowerCase() === lowerCity) {
      return coords;
    }
  }
  
  // Try partial match
  for (const [key, coords] of Object.entries(cityCoordinates)) {
    if (key.toLowerCase().includes(lowerCity) || lowerCity.includes(key.toLowerCase())) {
      return coords;
    }
  }
  
  // Fallback to country center
  return getCountryCenter(countryCode);
}

